    public void displayTable() {
        table.setModel(myModel);
        myModel.setRowCount(0); 
        List<Voucher> listVoucher = new VouchersDAO().GetListVoucher();
        for (int i = 0; i < listVoucher.size(); i++) {
            Voucher voucher = listVoucher.get(i);
            String discount = voucher.getDiscount() + "%";
            Object[] dt = {i + 1,voucher.getName(), discount};
            myModel.addRow(dt);
        }
    }
